class Person
    def initialize(age, name = 'Unknown', parent_permission = true)
        @id
        @name = name
        @age = age
        @parent_permission = parent_permission
    end

    def id
        @id
    end
    
    def name
        @name
    end

    def age
        @age
    end

    def name = (value)
        @name = value
    end

    def age = (value)
        @age = value
    end
end